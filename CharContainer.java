package com.gmail.s12348.evgen;

public class CharContainer {
	private char ch;
	private int n;

	public CharContainer(char ch, int n) {
		super();
		this.ch = ch;
		this.n = n;
	}

	public CharContainer() {
		super();
	}

	public char getCh() {
		return ch;
	}

	public void setCh(char ch) {
		this.ch = ch;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}

}
